let vel = 130
let limite = 150

console.log(`Bom dia! Você passou aqui à ${vel}km/h.`)
if(vel > limite){
    console.log(`Ultrapassou a velocidade permitida que é de ${limite}km/h, e foi multado!`)
}else{
    console.log(`Tudo certo, você está dentro do limite de velocidade que é ${limite}`)
}
console.log('Dirija sempre usando cintos de segurança! Tenha um bom dia.')